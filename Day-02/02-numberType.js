function numberType(num){
    if (num>0){
        console.log("Entered Number is Positive Number");
        
    }
    else if (num === 0){
        console.log("Entered Number is Neutral Number");
        
    }
    else if (num<0){
        console.log("Entered Number is Negative Number");
        
    }
    else {
        console.log("Inputs are not Entered");
        
    }
}
